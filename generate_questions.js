// 125 سؤالاً كمياً حقيقياً
const quantitative = [];
for (let i = 0; i < 125; i++) {
  const a = Math.floor(Math.random() * 18) + 2;
  const b = Math.floor(Math.random() * 18) + 2;
  const type = i % 5;
  let q, correct, w1, w2, w3;
  if (type === 0) {
    correct = a * b;
    q = `ما ناتج ضرب ${a} × ${b}؟`;
    w1 = correct + 3; w2 = correct - 2; w3 = a + b;
  } else if (type === 1) {
    const total = a * b;
    q = `اشترى محمد ${a} كتباً بمجموع ${total} ريال، فكم سعر كتاب واحد؟`;
    correct = b; w1 = b + 2; w2 = b - 1; w3 = b * 2;
  } else if (type === 2) {
    const speed = a * 10, hours = (b % 5) + 1;
    q = `سيارة تسير بسرعة ${speed} كم/س لمدة ${hours} ساعات، فكم المسافة؟`;
    correct = speed * hours; w1 = correct + 10; w2 = correct - 10; w3 = speed + hours;
  } else if (type === 3) {
    const quarter = a * 5;
    q = `إذا كان ربع مبلغ ما هو ${quarter} ريال، فما المبلغ كاملاً؟`;
    correct = quarter * 4; w1 = quarter * 3; w2 = quarter * 2; w3 = correct + 10;
  } else {
    const pct = [10,20,25,50][b % 4];
    const base = a * 20;
    correct = base + (base * pct / 100);
    q = `اشترى علي بضاعة بـ ${base} ريال وباعها بربح ${pct}٪، فكم ثمن البيع؟`;
    w1 = correct + 5; w2 = correct - 5; w3 = base + pct;
  }
  quantitative.push({ q, correct: String(correct), w1: String(w1), w2: String(w2), w3: String(w3), cat: "كمي" });
}

